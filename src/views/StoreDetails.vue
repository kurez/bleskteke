      data () {
      return {
        details: [],
        userpkid: '',
        item_quantity: '0',
        slug :this.$route.params.slug
      }
      },
      created() {
        this.getDetailsData()
    },
  methods: {
    getDetailsData(slug) {
        axios.get('https://blesketke-live.com/api/v1/store/details/'+this.slug+'/', { headers: { Authorization: `Bearer ${this.$store.state.accessToken}` } }) // proof that your access token is still valid; if not the
        // axios getAPI response interceptor will attempt to get a new  access token from the server. check out ../api/axios-base.js getAPI instance response interceptor
          .then(response => {
            // console.log('GetAPI successfully got the categories')
            this.details = response.data // store the response data in store
            // console.log(response.data)
          })
          .catch(err => { // refresh token expired or some other error status
            console.log(err)
          })
        // console.log( slug)
    },
    addItemToCart (item) {
        axios.get('https://blesketke-live.com/api/v1/auth/users/me/', { headers: { Authorization: `Bearer ${this.$store.state.accessToken}` } }) // proof that your access token is still valid; if not the
        // axios getAPI response interceptor will attempt to get a new  access token from the server. check out ../api/axios-base.js getAPI instance response interceptor
          .then(response => {
            // console.log('GetAPI successfully got user details')
            this.userpkid = response.data.pkid // store the response data in store
            // console.log(response.data.pkid)
          })
          .catch(err => { // refresh token expired or some other error status
            console.log(err)
          })

    
        axios.post('https://blesketke-live.com/api/v1/carts/add_cart/',
            {
            "user": this.userpkid,
            "variations": item.variation[0].id,
            "product": item.id,
            "cart": '',
            "quantity": this.item_quantity,
            "is_active": true
            },
        { headers: { Authorization: `Bearer ${this.$store.state.accessToken}` } }) // proof that your access token is still valid; if not the
        // axios getAPI response interceptor will attempt to get a new  access token from the server. check out ../api/axios-base.js getAPI instance response interceptor
          .then(response => {
            console.log('GetAPI successfully posted item to cart')
            this.$router.push({ name: 'cart' })
            // this.details = response.data // store the response data in store
            console.log(response.data)
          })
          .catch(err => { // refresh token expired or some other error status
            console.log(err)
          })
  

    }
